import { defHttp } from '/@/utils/http/axios';
import { useMessage } from '/@/hooks/web/useMessage';

const { createConfirm } = useMessage();

enum Api {
  list = '/iot/manage/alertLog/list',
  details = '/iot/manage/alertLog/queryById',
  save = '/iot/manage/alertLog/add',
  edit = '/iot/manage/alertLog/edit',
  deleteOne = '/iot/manage/alertLog/delete',
  deleteBatch = '/iot/manage/alertLog/deleteBatch',
  importExcel = '/iot/manage/alertLog/importExcel',
  exportXls = '/iot/manage/alertLog/exportXls',
}
/**
 * 导出api
 * @param params
 */
export const getExportUrl = Api.exportXls;
/**
 * 导入api
 */
export const getImportUrl = Api.importExcel;
/**
 * 列表接口
 * @param params
 */
export const listAlert = (params) => defHttp.get({ url: Api.list, params });

/**
 * 详情接口
 * @param params
 */
export const getDetails = (params) => defHttp.get({ url: Api.details, params });

/**
 * 删除单个
 */
export const deleteOne = (params, handleSuccess) => {
  return defHttp.delete({ url: Api.deleteOne, params }, { joinParamsToUrl: true }).then(() => {
    handleSuccess();
  });
};
/**
 * 批量删除
 * @param params
 */
export const batchDelete = (params, handleSuccess) => {
  createConfirm({
    iconType: 'warning',
    title: '确认删除',
    content: '是否删除选中数据',
    okText: '确认',
    cancelText: '取消',
    onOk: () => {
      return defHttp.delete({ url: Api.deleteBatch, data: params }, { joinParamsToUrl: true }).then(() => {
        handleSuccess();
      });
    },
  });
};
/**
 * 保存或者更新
 * @param params
 */
export const saveOrUpdate = (params, isUpdate) => {
  const url = isUpdate ? Api.edit : Api.save;
  return defHttp.post({ url: url, params });
};
